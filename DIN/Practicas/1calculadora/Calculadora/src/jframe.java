    private void igualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_igualMouseClicked
        if(op==1){
            num2=num1+parseInt(entrada.getText());
            salida.setText(num2 + "");
            entrada.setText("");
        }else if (op==2){
            num2 = num1 - parseInt(entrada.getText());
            salida.setText(num2 + "");
            entrada.setText("");
        }else if (op==3){
            num2 = num1 * parseInt(entrada.getText());
            salida.setText(num2 + "");
            entrada.setText("");
        }else{
            num2 = num1 / parseInt(entrada.getText());
            salida.setText(num2 + "");
            entrada.setText("");
        }
    }//GEN-LAST:event_igualMouseClicked

